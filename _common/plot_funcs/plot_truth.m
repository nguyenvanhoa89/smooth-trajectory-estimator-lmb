function plot_truth(truth,t1,t2)
  
  f = figure;
  a = axes('Parent',f,...
           'XGrid','on',...
           'YGrid','on',...
           'Box','on',...
           'DataAspectRatio',[1 1 1]);
  xlabel(a,'X Position');
  ylabel(a,'Y Position');
  
  X_track = extract_tracks(truth.X,truth.track_list,truth.total_tracks);
  if nargin == 3
    X_track = X_track(:,t1:t2,:);
  end
  
  for i = 1:size(X_track,3)
    start_idx = find(~isnan(X_track(1,:,i)),1,'first');
    end_idx = find(~isnan(X_track(1,:,i)),1,'last');
    if ~isempty(start_idx)
      line('Parent',a,...
           'XData',X_track(1,start_idx:end_idx,i),...
           'YData',X_track(3,start_idx:end_idx,i),...
           'Color','k',...
           'LineStyle','-');
      line('Parent',a,...
           'XData',X_track(1,start_idx,i),...
           'YData',X_track(3,start_idx,i),...
           'Color',[0 0 0],...
           'MarkerFaceColor',[0 1 0],...
           'Marker','o',...
           'MarkerSize',8,...
           'LineStyle','none');
      line('Parent',a,...
           'XData',X_track(1,end_idx,i),...
           'YData',X_track(3,end_idx,i),...
           'Color',[0 0 0],...
           'MarkerFaceColor',[1 0 0],...
           'Marker','s',...
           'MarkerSize',8,...
           'LineStyle','none');         
    end
  end
  
end
